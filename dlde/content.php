			<h3 class="h3main"><a href="/dlde/whitepaper.php">About the Project</a></h3>
			<p>The <strong>Digital Learning and Development Environment</strong> (DLDE) centers on a web-based creative workspace where you can construct a presentation called a Digital Learning Object (DLO) that can combine text, video, and even audio materials.</p>
<p>The purpose of this workspace is to promote wider use of the <a href="http://digital.lib.wayne.edu">Digital Collections</a> of the Wayne State University Library System <strong>(WSULS)</strong> for teaching and learning.  The DLDE bridges the Collections and your DLO in the following ways. It:

	<ul class="about">
        <li><a href="/dlde/how-to/part-one/">Introduces users</a> to WSULS Digital Collections</li>
        <li><a href="/dlde/how-to/videos/portfolio.php">Demonstrates creating portfolios</a> of images</li>
        <li>Provides a <a href="/dlde/index.php#bridge">bridge tool for moving portfolios into templates</a> for building multimedia presentations in PowerPoint or HTML, called <strong>digital learning  objects (DLO)</strong></li>
        <li>Provides <a href="/dlde/how-to/part-three/">examples of DLOs and strategies</a> for building effective DLOs
        <li>Offers <a href="/dlde/archive/">research strategies</a> through the Teaching &amp; Learning Archive to develop materials to accompany the images.</li>
    </ul>
    
