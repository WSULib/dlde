<h2 class="white-head">6.0 Long-term Significance of Project</h2>
<p>The long-term impact of this project will be two-fold. The first impact is technical in nature, though it bridges technological and instruction design challenges. The final product removes a number of barriers that have prevented wider use of <a href="http://digital.lib.wayne.edu">Wayne State’s Digital Collections</a>. Because it provides a system for integrating tools with instructional principles and support strategies, along with a method for creating Digital Learning Objects (DLOs), the prototype suggests an approach that can be applied to other repositories.</p>

<div class="indent">
<h3>Wayne State University Impact</h3>
<p>
The implementation at Wayne State University includes an announcement and presentation to the Digital Humanities Collaboratory in January, 2010 and the launch of the DLDE on the <a href="http://digital.lib.wayne.edu">Wayne State University Digital Collections</a> website for use by all.  Ongoing support for use of the DLDE is available through consultations and workshops sponsored by the <a href="http://www.otl.wayne.edu">Office for Teaching & Learning</a> and the Wayne State University Library System partners on the Team will continue to provide expertise and instruction as the project and associated research moves forward.  Impact of the DLDE will be measured as part of the summative evaluation and will be supported and developed on a long term basis.
</p>

<h3>National Impact</h3>
<p>
We find ourselves in a pivotal point in time with respect to the relationship of humanities scholarship and technology.  The nature of research in the academic and scholarly community continues to develop at a rapid pace, and incorporates increasingly sophisticated technological tools, redefining our concept of collections and collection development.  As we create digital archives and library collections in collaboration with scholars, many questions come to mind, including the long term preservation of the various formats, usability of the created online systems, and the increasingly central place of these digital collections within the scholarly community.  Most art and humanities collections exist in academic institutions to which scholars must travel far distances.  The goal of many academic libraries and archives is to move these collections out into the digital environment, and make them accessible to view worldwide for the first time.  This new access will likely create additional demand for an efficient way to import images and metadata into digital learning objects.  To assist faculty and students, the software design of the DLDE will be made available to the academic and scholarly community, and all are welcome to use the unique technology that was developed as part of this project. Plans are underway to begin this distribution through a <a href="http://creativecommons.org/">Creative Commons license.</a></p>
</div>

<p>The second and equally complex impact is intellectual in nature. This project was a testbed for defining a philosophy of digital teaching and learning in the <a href="http://www.trc.wayne.edu">Technology Resource Center's (TRC)</a> expanding portfolio of <i><a href="http://www.otl.wayne.edu/dpel">Digital Partnerships for Engaged Learning</a></i>. Three concepts have emerged that are becoming cornerstones of an integrated philosophy in the TRC.</p>

<div class="indent">
<h3>Constructivist Learning</h3>
 <p>The “constructivist” theory of learning holds that individuals construct new knowledge from their experiences. It emphasizes active involvement, exploration and discovery, inferences, and reflexivity, not passive delivery and receipt of pre-determined meanings. Teachers become facilitators, and students make meaning through interactions with peers and course environments. Students need a structure providing guidance and parameters. They also need sound scholarly resources. Yet, the learning process is generative and open-ended. Student assignments which include the creation of DLOs provide a dynamic and flexible method for constructing new understandings, sharing and assessing them with others, and arriving at a more complex understanding of the topics and subjects being engaged in any educational setting.</p>

<h3>Digital Storytelling</h3>
<p>The Digital Learning and Development Environment (DLDE) project has clearly shown that digital learning objects (DLOs) can be an effective method of constructivist learning.  A new area that lends itself to constructivism is digital storytelling, defined by Leslie Rule of the <a href="http://www.storycenter.org/index1.html">Center for Digital Storytelling</a> as, “the modern expression of the ancient art of storytelling” (<a href="http://www.storycenter.org/stories">2009</a>).  The fusion of technology with storytelling can be a powerful collaborative educational experience in which students learn how to: research, write, interview, present, assess, solve problems, and work with others.  Margaret Nellis of the University of Wisconsin notes "digital storytelling provides avenues for experiential learning, reflective practice, effective communication, and building community in the classroom and beyond" (<a href="https://academictech.doit.wisc.edu/ideas/digitalstorytelling">2009</a>).  In a digital age we need new literacies that balance the mastery of technical tools with the learning skills necessary to construct and understand narratives of human experience. The foundation built with the DLDE project can serve as a starting point for exploring the links between constructivist learning and digital storytelling. 
</p>


<h3>Architected Meaning</h3>
<p>From the beginning we speculated that the abstracting and scaffolding capacity of DLOs would be complementary to Strain & Van Hoosier-Carey’s concept of “architected meaning.” Interdisciplinary connections are fostered by a hypermediated web of juxtapositions and associations that catalyzes humanities and social science methodologies, facilitating an interactive approach to studying cultural history. Architected meaning stimulates a new relationship between technology and humanities as well, grounded in the intersections of textuality, visuality, and aurality in production and use of cultural objects (“Eloquent Interfaces.” <a href="http://mitpress.mit.edu/catalog/item/default.asp?ttype=2&tid=9607">Eloquent Images</a>, ed. M. Hocks and M. Kendrick, 257-81. Cambridge: MIT Press, 2003).</p> 

<p>The concept of “architected meaning” also has technological and organizational implications for this project. It has been a seedbed for an integrative philosophy of digital media and learning being developed in a number of other projects.</p>

<p>Architected meaning has a final significance for this project because of its location. The staff of this project all work within a suite of offices known as the <a href="http://www.trc.wayne.edu">Technology Resource Center (TRC)</a>. The TRC is  part of the <a href="http://www.lib.wayne.edu">Wayne State University Library System</a>, though another unit that reports to the Office of the Provost, the <a href="http://www.otl.wayne.edu">Office for Teaching and Learning</a>, is also housed physically within the TRC. The co-location and shared mission of these two units is vital to the quality of this and many other projects. Both units are devoted to bridging new technologies and instructional design. Their long-term alliance will be key to nurturing future projects.</p>

<p>For example, <i><a href="http://www.otl.wayne.edu/dpel/">Digital Partnerships for Engaged Learning</a></i> is a joint initiative of the Wayne State University Library System and the Office for Teaching & Learning which fosters purposeful integration of digital media in teaching and learning by bridging faculty and TRC expertise, building faculty learning communities, and collaborating with other institutions in the community.  The partnership incorporates the study and use of new technologies in teaching and research, the impact of new technologies on the creation and preservation of scholarly collections, and the state of the art regarding intellectual property rights in the academy.</p>


