  <h4><em>Virtual Motor City</em></h4>
  
  Since the 1880s, the <em>Detroit News</em> has been capturing
  the spirit of Detroit through photography and articles.
  The newspaper donated over 800,000 images from its
  photo &quot;morgue&quot; to the Walter P. Reuther Library on the
  Wayne State University campus. In 1997, the WSULS
  and the Reuther Archive collaborated to digitize more
  than 15,000 of the historical photographs. The <em>Virtual
  Motor City</em> project also digitized a number of 1920s
  newsreels from the <em>Detroit News</em>.
  The repository enables users to
  browse the entire collection of digitized photographs
  and captions depicting Detroit’s vibrant cultural history.
  You can browse by decade,from the 1890s through
  the 1980s, or by topic.</div>
