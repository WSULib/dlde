<h2 class="h2main">Building and Assessing Digital Learning Objects</h2>
<h2>What is a  Digital Learning Object?</h2>
<p> New Media Consortium (NMC) defines <strong>digital learning objects </strong>(DLOs) as small units that can be  fitted together in any number of ways to produce customized experiences tied to an educational objective. They can cover a single topic or chunk of instruction, and contain clear objectives, explanations and examples, and assessment/feedback.  A DLO includes small pieces--i.e., building block (photos, text, sound, video) that separately do not constitute a comprehensive learning experience.  A DLO is often likened to LEGO building blocks.  When the individual pieces are connected and arranged, they create useful items such as houses, bridges, etc.  They make use of media in order to support instruction and multi-modal learning. 
</p>

<p>Learning objects are flexible, cost-effective, reusable and customizable.  DLOs in essence triangulate content, interface, and metadata:

<br />
<table>
  <tr>
    <th class="odd">Content</th>
    <th class="odd">Interface</th>
    <th class="odd">MetaData</th>
  </tr>
  <tr>
    <td class="tdclass">Documents, pictures, simulations, movies, sounds, etc.</td>
    <td class="tdclass">Medium through which users interact with objects with alternative formats</td>
    <td class="tdclass">Information about objects (author, creator, subject area, copyright)</td>
  </tr>
</table>

