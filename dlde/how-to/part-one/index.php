<h4 style="text-align: left;"><em>Digital Dress: 200 Years of Urban Style</em></h4>

The <em>Digital Dress</em> collection enables users to browse images and accompanying metadata of clothing
and accessories depicting the history of style from 1800 to 2000. <em>Digital Dress</em> spans from the beginning of the fur  trade, through the advent of the automobile, the impact of the cultural revolution of the 1960s, and the role of technology in fashion through the latter half of the twentieth century.  The images show how style mirrors changes in popular culture, industrialization, inventions, war, poverty, labor, status, occupation and group identity.
