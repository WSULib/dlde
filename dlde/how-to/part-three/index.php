<h2>What are the Advantages of Digital Learning Objects?</h2>
<p>DLOs are:
<ul class="about">
<li>Flexible: Can contain clear assignments or be used open-ended and support instruction and multi-modal learning</li>
<li>Reusable: Can be used for single discipline or cross disciplinary topics and can be scalable for small or large presentations</li>
<li>Customizable: Can be viewed singly or grouped, arranged, or chunked to form patterns or to present different perspectives and can accommodate different learning styles</li>
<li>Cost-effective: Offer efficient method of information presentation</li>
</ul>
</p>

<p>
DLOs can:
<ul class="about">
<li>Assist understanding of more abstract and complex concepts</li>
<li>Promote engagement, proficiency</li>
<li>Support retention</li>
<li>Permit scaffolding with other objects</li>
<li>Be interactive</li>
</ul>

</p>

<br />
<br />

<h2>What Components Belong in an Effective DLO?</h2>
<p>An effective DLO will contain, at the very least:
<ul class="about">
<li>Introduction</li>
<li>Learning Objective(s)</li>
<li>Explanation, description, background</li>
<li>Examples</li>
<li>Relationship to other relevant concepts</li>
<li>Practice and Assessment</li>
<li>Summary</li>
</ul>
</p>

