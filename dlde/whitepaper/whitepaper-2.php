<h2 class="white-head">2.0 Project Background</h2>

<p>
<a href="http://digital.lib.wayne.edu"><i>Virtual Motor City</i> and <i>Digital Dress</i></a> were created by <a href="http://digital.lib.wayne.edu">Wayne State University Library System (WSULS)</a> in collaboration with its partner institutions, including the <a href="http://reuther.wayne.edu">Wayne State University Reuther Archives</a>, the <a href="http://www.detroithistorical.org/">Detroit Historical Museum</a>, the Wayne State University Historic Costume Collection, the <a href="http://www.oakland.edu/mbh/">Meadow Brook Hall</a> and <a href="http://thehenryford.org">The Henry Ford</a>. The collections feature cultural objects ripe for study within a wide array of disciplines and interdisciplinary fields.</p>


<p class="thumbs">
<img class="border" src="http://www.lib.wayne.edu/inc/img/digitalcollections/vmc/vmc-thumb.jpg" />
<a href="http://www.lib.wayne.edu/resources/digital/projects/vmc.php">Virtual Motor City</a><br />A subset of photos from more than 800,000 selected negatives from the Detroit News photo morgue. The photos document American social history, including the automobile industry, business and labor, architecture, politics, sports, and entertainment from 1868 to 1980. </p>

<p class="thumbs">
<img class="border" src="http://www.lib.wayne.edu/inc/img/digitalcollections/digdress/dd-thumb.jpg" /><a href="http://www.lib.wayne.edu/resources/digital/projects/digitaldress.php">Digital Dress</a><br /> 5,000 digital images of Detroit-worn clothing and accessories documenting changes in popular culture, industrialization, inventions, labor organization, and Detroit’s socio-economic, racial, and ethnic mix during the period spanning 1800 and 2000. </p>

<p>The Collections are also accessible from the WSULS homepage at <a href="http://digital.lib.wayne.edu">http://digital.lib.wayne.edu</a>. They may also be previewed in a short <a href="http://www.lib.wayne.edu/sites/hastac/promovid.html">Digital Collections Video on the WSU/HASTAC website</a>.</p>

<p>Digitization of the objects was made possible by earlier external grants from the <a href="http://www.imls.gov">Institute of Museum and Library Services</a> and the <a href="www.michigan.gov/libraryofmichigan">Library of Michigan</a>. </p>

<h2 class="white-head">2.1 Need for the Project </h2>
<p>The Collections are accessed thousands of times per month. However, their use in online instruction is limited by three barriers that have resulted nationally in widespread underuse of digital repositories (Use and Users of Digital Resources, 2005-2006: <a href="http://cshe.berkeley.edu/publications/publications.php?id=211">http://cshe.berkeley.edu/publications/publications.php?id=211</a>; Using Digital Images in Teaching and Learning, 2006: <a href="http://www.academiccommons.org/imagereport">http://www.academiccommons.org/imagereport</a>). </p>

<p>The <strong>first barrier</strong> is technological. Early adopters and faculty innovators are using the latest technologies (e.g., blogs and wikis, synchronous “live classrooms,” messaging, digital video, and virtual worlds). However, their usage is sporadic and inconsistent.</p>

<p>The <strong>second barrier</strong> is inadequate planning and preparation for teaching with new technologies. They are often incomplete, and feedback from users is insufficient. Many universities and colleges provide faculty development for instructional design, pedagogies, and technology use. Rarely, though, are all services co-present and integrated into the design of particular projects. </p>

<p>The <strong>third barrier</strong> is an inadequate knowledge base for using digital collections. Information is often ad hoc, resulting in hit-and-miss use of online resources by both faculty and students. It is not necessarily credible, either, lacking grounding in principles of sound research.</p>


<p>The project addressed all three barriers by taking the next step in instructional design for using digital collections. It created tools for importing images and metadata information about them from the WSULS Collections into a new web-based Digital Learning and Development Environment. The Environment, called the <a href="#sandbox">Sandbox*</a> is a workspace where faculty and students can create multi-media Digital Learning Objects (DLOs). A Teaching and Learning Archive in the DLDE guides the process of incorporating credible research materials into DLOs, and also allows users to contribute and share their own DLOs and research materials. Although some resources are restricted to Wayne State faculty and students, other institutions can duplicate the pathway model we devised for bridging national and local research resources.</p>

<p><small><i id="sandbox">*Sandbox</i> is a term in software development referring to a virtual environment that allows experimental testing of programs, codes, and other features before going “live” with a developed model.
</small></p>
  
