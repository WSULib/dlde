<h2 class="white-head">1.0 Project Staff</h2>
<p class="title">
<img class="border" src="images/mein.jpg" />
<i>Principal Investigator, Dr. Nardina Mein</i><br /> Director of New Media and Information 
Technology &amp; Interim Director of the Office for Teaching and Learning </p>

<p>
Dr. Mein is the Director of New Media and Information Technology at Wayne State University, and is the overall manager for a large portfolio of digital projects, with multiple roles of design, development, implementation, and collection management, preservation and access on the web.  She also plays a leadership role in the Digital Partnerships for Engaged Learning, an innovative portfolio of teaching and learning focused digital projects managed within the Technology Resource Center which seeks to foster the use of digital technologies in teaching and learning through faculty collaboration. </p> 

<p>A major part of Dr. Mein’s responsibilities include  media development for instruction, classroom technology design and support,  library systems,  library public and staff computing and evaluation of teaching and learning technologies for the University.  Dr. Mein has written several successful grants, including most recently the Digital Media Learning Environment, a National Endowment for the Humanities grant that supports faculty and students as they create learning objects with images from digital collections. She was also Principle Investigator on the recently completed Library of Michigan Digital Preservation grant, which supported the digitization of images from the Detroit News Collection currently housed in the Reuther Archives at the University.</p>  

<p>Dr. Mein received a BA from Michigan State University and a MSLS from Wayne State University in Detroit, Michigan.  She received a doctorate in Instructional Technology from Wayne State University in May 2005, specializing in instructional design and message design for online instruction.
</p>

<p class="title">
<img class="border" src="images/klein.jpg" />
<i>Co-Principal Investigator, Dr. Julie Thompson Klein</i><br /> Professor of Humanities
 in English/Interdisciplinary Studies and Faculty Fellow in the Office for Teaching and Learning
</p>

<p>Klein was responsible for conceptual oversight, submission of the grant proposal and documents authorizing user testing, hiring of initial student assistants, writing and editing website text, coordination of subteams and meetings, and the Teaching & Learning. Archive.</p>

<p>Holder of Ph.D. and Doctor of Arts degrees in English from the University of Oregon, Klein is Professor of Humanities in English and Faculty Fellow in OTL. She has also held visiting posts in Japan, New Zealand, and Nepal, and was Senior Fellow at the Association of American Colleges & Universities. She directs the Digital Humanities Collaboratory on campus and is a member of the national Steering Committee of HASTAC. Her areas of expertise are humanities, American cultural studies, and interdisciplinary history, theory, and practice.</p>

<p>Klein is past president of the Association for Integrative Studies and former editor of <i>Issues in Integrative Studies</i>. She is a member of Wayne State’s Academy of Scholars and received the University’s highest awards for teaching and research excellence. She also received the Kenneth Boulding Award for outstanding scholarship on interdisciplinarity and the Yamamoorthy & Yeh Transdisciplinary Distinguished Achievement Award. Klein has lectured throughout Europe, North America, Latin America, and Australia.</p>

<p>Her publications include numerous articles and chapters and the authored and edited books <i>Interdisciplinarity</i> (1990), <i>Interdisciplinary Studies Today </i>(1994), <i>Crossing Boundaries</i> (1996), <i>Mapping Interdisciplinary Studies </i>(1999), <i>Transdisciplinarity </i>(2001), <i>Interdisciplinary Education in K-12 and College </i> (2002), <i>Humanities, Culture, and Interdisciplinarity</i> (2005), and <i>Creating Interdisciplinary Campus Cultures</i> (2010). She is also Associate Editor of the <i>Oxford Handbook of Interdisciplinarity</i>, co-editor of the University of Michigan Press series Digital Humanities@digitalculturebooks, and is working on a book on Mapping Digital Humanities.
</p>



<h2 class="white-head">Team Members</h2>
<ul class="team">

<li><p class="title2">
<img class="border" src="images/aluzzo.jpg" />
Adrienne Aluzzo, Metadata Librarian
</p>
<p>Ms. Aluzzo developed the metadata mapping, normalization, and indexing.  She is an experienced metadata and cataloging librarian who has also considerable knowledge in the development of digital collections.  Aluzzo brought extensive experience from other projects, insuring that metadata for the images and learning objects were integrated into the database and the overall plan for instructional design guidelines.  She also created digital learning objects to serve as examples for the project.</p>

<p>At Wayne State University, Ms. Aluzzo oversees the metadata work of many of the Library’s digital collections.</p>

<p>Prior to her work at Wayne State University, Ms. Aluzzo helped develop the digital program at the Detroit Public Library, working on the Hackley African American Sheet Music Project, the Packard and Glidden projects for The Making of Modern Michigan and Early Detroit Images from the Burton Historical Collection at the Detroit Public Library.  Ms. Aluzzo also was an original cataloger of sheet music, rare books, audio cds and videos.  For many years she was the Trainer/User Support Specialist for Tip Database & Subscriptions, an Information & Referral database and service of the Detroit Public Library.</p>

<p>Ms. Aluzzo managed the Interlibrary Loan function of the Archives of American Art (Smithsonian Institution), which was an international program.  She also provided research assistance to scholars, processed archival collections, and created finding aids and catalog records for the collections.</p>

<p>Ms. Aluzzo received a BA and MSLS and Graduate Certificate in Archival Administration from Wayne State University in Detroit, Michigan.  She is currently working on a Master’s degree in Cultural Anthropology also at WSU.
</p>
</li>

<li><p class="title2">
<img class="border" src="images/armstrong.jpg" />Dr. Anne-Marie Armstrong, Instructional Designer</p>
<p>
Instructional Designer in OTL, designed online learning components of instruction and worked with faculty. She has had extensive experience building instruction in higher education, museum, business and military settings, and is particularly skilled in the design and development of learning objects and learning objectives.
</p>

<p>As instructional designer for the DLDE Project, Dr. Anne-Marie Armstrong produced both training and instructional materials on Learning Objects, Digital Learning Objects, Media Objects and on the use of the digital collections for instructional and training purposes.  Dr. Armstrong researched the current literature and surveyed faculty on the use of learning objects.  One of products of her research is the Checklist for Assessing Media Projects and Learning Objects which is incorporated into the DLDE project.  She worked closely with the web designers to ensure the inclusion of good human factors and universal design principles in the final products.  She also provided technical training to the project team when needed.  Using Flash and Camtasia, Dr. Armstrong designed, developed and delivered samples and presentations both online and at regional and national academic conferences.  
</p></li>

<li><p class="title2">
<img class="border" src="images/decker.jpg" />
Matthew Decker, Associate Director of Digital Library Initiatives</p>
<p>
Matthew Decker has served in several job roles for Wayne State University.  He currently is the Associate Director of Digital Library Initiatives for the New Media and Information Technology department of the WSU Library System.  Matthew was involved in the the two digitization grants that originally created the Virtual Motor City and Digital Dress collections that are used in this project.
</p>
<p>
Matthew provided the overall supervision and general guidence for the technical aspects of this project.  He also provided the initial hiring and project management roles which were later assumed by Jonathan McGlone.  During the evaluation stage, he helped with conducting talk-aloud sessions.
</p>
<p>
Matthew received a Bachelors of Science in Information Systems Management from Wayne State University.
</p>
</li>

<li><p class="title2">
<img class="border" src="images/mcglone.jpg" />
Jonathan McGlone, Web-Librarian: Digital Projects</p>
<p>
As Web Librarian for Wayne State University Library System, McGlone manages WSU’s digital collections and institutional repository.  His areas of research interest are in digital collection usability, open access, and scholarly communication.  Received B.A. from Calvin College in Film Studies/Communications (2005) and his M.L.I.S. from Wayne State University (2008).</p>

<p>
For the Digital Learning and Development Environment, McGlone acted as project manager, overseeing the design and development of the bridge to WSU digital collections, the Digital Learning and Development Environment website, and interactive Teaching and Learning Archive.  In addition, McGlone identified the need to improve accessibility to Virtual Motor City (VMC), initiating changes to VMC metadata and interface.  McGlone also participated in the usability study and assessment of the Digital Learning and Development Environment and presented the project with DLDE team members Klein and Armstrong at EDUCAUSE 2009 Midwest Regional Conference in Chicago, IL.
</p>

</li>

<li><p class="title2">
<img class="border" src="images/neds-fox.jpg" />Joshua Neds-Fox, Web-Librarian</p>

<p>Joshua Neds-Fox, Web Librarian for the Wayne State University Library System, oversees the libraries' various websites -- designing, managing and maintaining the libraries' online presence, and working with the larger library system to bring new ideas online. He is responsible, in collaboration with the Digital Library Initiatives Team, for designing and developing the most recent incarnation of the WSU Library website, and led that project from inception to evaluation. Joshua received his BFA in Theater from Wayne State University (1996) and his MLIS from Kent State University (2003).
</p>
<p>
Working from a skeleton plan researched and begun by Safwan Al-Omari, Joshua developed the initial code for the tool translating a portfolio from the online collections into a downloadable learning object. Joshua directed the two student programmers (Fady Banno / Sahana Bangalore Nagaraja) in further code refinement and in the initial design of the DLDE website, and completed the code that allows users to choose a second learning object format.
</p>
</li>


</ul>

