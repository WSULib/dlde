<h2 class="white-head">4.0 Evaluation</h2>

<p>The evaluation plan for the Digital Learning Development Environment included usability testing, heuristic, formative, summative and confirmative evaluation methods. </p>

<div class="phases">
<h4>Phase One</h4>
<p>The DLDE Team evaluated potential tools for feasibility, designed a prototype for the web environment and critiqued iterative drafts of the design and content for the web environment.</p>

<p>
The effectiveness of the Environment was reviewed in the process by collaborating with WSU <a href="http://slis.wayne.edu">School of Library and Information Science</a>.  Ideas and feedback from these sessions were incorporated into the DLDE design.
</p>

<p>A heuristic evaluation (evaluation of the design elements which impact perception and cognition such as font color and use of space.) of the DLDE web site began in phase one and continued throughout the design and development process, incorporating design decisions so that the wide audience could use the site successfully.
</p>

 </div>

<div class="phases">
<h4>Phase Two</h4>
<p>Formative evaluation continued as we evaluated the technical features of the PowerPoint tool, the web environment design and refined content for the web environment.</p>
</div>

<div class="phases">
<h4>Phase Three</h4>
<p>Usability testing took place as part of the formative evaluation strategy.  By using the “talk aloud protocol,” faculty input was gathered on how well the DLDE could be used to design Digital Learning Objects with images from the Digital Collections.  Faculty worked through the features of the DLDE to create a learning object to be used in a course they teach.  DLDE Team members sat alongside faculty testers, provided a brief orientation and training session, and recorded the process that faculty used to create a Digital Learning Object, including issues and “stuck points.”  We concluded by asking the following questions:  </p>

<ol>
<li>How easy was the tool to use?</li>
<li>How easy was it to search and transpot images from the Collection in order to create learning objects with images and metadata?</li>
<li>In what ways would this tool be of use to you for teaching and learning in general and in particular settings?</li>
<li>Were there any areas in which difficulties arose?</li>
<li>What advice would you give for improving this tool?</li>
</ol>

<p>Based on answers to these questions and each faculty tester's recorded experiences, the most frequent problems from the usability study stemmed from the interface design of the bridge tool. These problems were addressed first.  In addition, the web and metadata librarians were able to identify long-term issues with the digital collections not directly related to the project.</p>

<p>First, participants had varying degrees of difficulty remembering what step to take next.  Most frequently, participants did not know what step to take after creating a portfolio in DLXS.  As a result, the website and technical designers decided that the bridge tool needed to both simplify the explanation of the steps and incorporate each step into the interface to improve the guidance of users.</p>

<p>Second, participants had difficulty locating the bridge tool on the HTML page.  Despite its location on the top of the HTML page, the tool’s interface needed to be designed to make it appear more prominently on the page.  The designers decided that if the page design looked more like an application than a regular website, the bridge tool would become the most prominent portion of the page.  In addition, actions (such as selecting a portfolio to transform to PowerPoint) were placed directly with the step number to synchronize design.</p>

<p>Third, participants were interested in using the tool with collections other than Virtual Motor City and Digital Dress.  To provide for this need, designers included a list to all WSU digital collections that used the DLXS software and were compatible with the DLDE bridge tool (compatibility required a collection to be OAI-PMH compliant).</p>

<p>A more long-term and previously identified problem was that participants struggled with searching the Virtual Motor City.  Most frequently, participants were too specific in their search terms when first searching the Virtual Motor City.  Their first and second searches were unsuccessful, largely in-part to being too specific without knowing what topics the Virtual Motor City covered.  Part of this problem was created in the design of the usability study, which did not allow for alternative methods for accessing the collections, such as browsing by subject or topic.  While solutions to this problem are discussed in <a href="/dlde/whitepaper/wp-3.php#metadata">3.0 Project Design</a>, the team has suggested that production of original metadata could be increased by offering metadata practicums for Masters of Library Science students attending <a href="http://slis.wayne.edu">Wayne State University’s School of Library and Information Science</a>.</p>
</div>


<div class="phases">
<h4>Phase Four</h4>
<p>Including the no-cost extension until 31 October 2009, we incorporated feedback described above in Phase Three and continued ongoing technical testing and iterative drafts of content. The faculty evaluation focused on the portal as a teaching tool and individual preferences for using it. The team also conducted a final overall evaluation of the project and implemented improvements. </p>

<p>
Summative evaluation will continue beyond the period of the grant, as the completion of the DLDE is announced to the Wayne State University campus, and faculty begin using it in their instructional presentations and assignments.  Research will be conducted on the impact of the DLDE on teaching and learning processes, assessing student learning to determine whether instructional objectives have been met and the amount and kind of learning that has occurred. In addition, a study of learner and faculty preferences will be conducted. The student evaluation will focus on usefulness of instructional strategies and the constructivist learning model. The faculty evaluation will focus on the portal as a teaching tool and faculty preferences for using it as a means of developing image-based learning objects. 
</p>
</div>


