<h2 class="white-head">5.0 Dissemination Plan, Presentations, and Publications</h2>
<p>The dissemination plan spans both local and national levels. At the local level, the <a href="http://www.otl.wayne.edu">Office for Teaching and Learning (OTL)</a> will sponsor a campus-wide launch of the Digital Learning and Development Environment (DLDE) and DLO method in January 2010, as part of the OTL’s programming for the faculty learning community known as the <a href="http://www.otl.wayne.edu/support_dhc.php">Digital Humanities Collaboratory</a>. Team members will demonstrate the final product then offer follow-up training workshops and individual consultations. Two members of the faculty focus group have already pledged to use the Collections and DLDE in their classes during the 2009-2010 academic year, and that list is growing. <a href="http://www.lib.wayne.edu/services/computing/labs/sts/">The Student Technology Studio</a> will also be available to help students in building their own learning objects. In addition, the team will coordinate with our museum and archive partners who were involved with Virtual Motor City and Digital Dress on further dissemination through their professional networks and contacts in schools throughout the Detroit Metropolitan area. Through these means, we will continue building a Digital Collections Teaching Community. </p>

<p>At the national level, we will make the product available as a universally accessible web portal. Members of the team will also present the model and lessons learned to the design community at large in conference presentations for pertinent professional organizations, including EDUCAUSE, the Association of Educational Computing and Technology, the American Library Association, Coalition for Networked Information, and HASTAC (Humanities, Arts, Science, and Technology Advanced Collaboratory). During the grant period, we have already made two presentations of work-in-progress and have planned a presentation for 2010:</p>
<h3>Presentations</h3>
<ul class="diss">
<li>Demonstration of the DLDE project at <a href="http://net.educause.edu/Program/1020076">2009 Midwest EDUCAUSE conference</a> March 23-25 in Chicago, Illinois, with presentation materials online (<a href="http://tinyurl.com/educause-dlde">http://tinyurl.com/educause-dlde</a>). By team members McGlone, Armstrong, and Klein. </li>

<li>A poster session demonstration of the DLDE test site at the <a href="http://www.chass.uiuc.edu/Events/Entries/2009/4/19_HASTAC_III.html">2009 HASTAC III Conference</a>, held 19-21 April in Urbana-Champaign, IL. By team members Klein, Mein, Armstrong, and Ian Chapp (2008-2009 WSU HASTAC Scholar). We also showed the DLDE in a formal presentation on WSULS’s portfolio of <i><a href="http://www.otl.wayne.edu/dpel">Digital Partnerships for Engaged Learning</a></i>.</li>

<li>A presentation of the DLDE project accompanied by gathered data on usage in the classroom is planned for April 12-13, 2010 at the <a href="http://www.cni.org/">Coalition for Networked Information (CNI)</a> Task Force Meeting in Baltimore, Maryland.</li>

</ul>
<br />
<h3>Publications</h3> 
<ul class="diss">
<li>
The team plans to expand this White Paper into a collaborative publication in a hybrid print and digital format, to be submitted to a forum such as the <i><a href="http://ijlm.net/">International Journal of Learning and Media</a></i> or <i><a href="http://digitalhumanities.org/dhq/">Digital Humanities Quarterly</a></i>.</li>

<li>Klein will be citing the DLDE project in an analysis of digital teaching and learning in a new book on Mapping Digital Humanities, a volume slated for the new University of Michigan Press series <a href="http://www.digitalculture.org/">Digital Humanities@digitalculturebooks</a>. McGlone is the technical designer for this hybrid print and digital publication, and Tuffin served as a research assistant (using private research funds).</li>

</ul>

