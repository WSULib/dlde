<h2 class="white-head">Project Abstract</h2>
<p>Wayne State University’s <a href="/dlde/"><i>Digital Learning and Development Environment</i></a> was a research and development project aimed at developing a prototype for a systematic approach to digital learning using image repositories. The repositories used in the project were two of the Wayne State University Library System’s (WSULS) <a href="http://digital.lib.wayne.edu">Digital Collections</a>: <a href="http://dlxs.lib.wayne.edu/cgi/i/image/image-idx?c=vmc"><i>Virtual Motor City</i></a> and <a href="http://dlxs.lib.wayne.edu/cgi/i/image/image-idx?page=searchgroup;g=costumegroupic"><i>Digital Dress</i></a>. The Collections are web portals providing universal access to digitized objects of cultural history from dispersed holdings of WSULS’s institutional partners. The project integrates easy-to-use technical tools with instructional design principles and resources for digital teaching and learning. The result is a replicable web environment where faculty and students can use accessible tools to easily create Digital Learning Objects (DLOs) from collections of digital images.  The unique design of the Environment places images from the Library's Digital Collections in context with a tool that downloads the images into a learning object and also provides expert advice in the design of effective digital media for instructional presentations and assignments.</p>

