<h3 class="h3main"><a href="/dlde/archive/">Teaching &amp; Learning Archive</a></h3>
<p>The <a href="/dlde/archive/">Teaching &amp; Learning Archive</a> is a dynamic environment filled with cultivated resources to guide uesrs in creating, using, and assessing Digital Learning Objects (DLOs).  In addition to library and online resources for incorporating research materials into DLOs, the Teaching &amp; Learning Archive features example DLOs--we encourage<a href="/dlde/archive/?page_id=21">you to contribute their own DLOs</a> and other resources created with the DLDE bridge tool.</p>

