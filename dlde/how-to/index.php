	<h2 class="h2main">How To...</h2>

<p>This section of the Digital Learning and Development Environment (DLDE) provides instruction aimed at helping you improve use of WSU Digital Collections, the Sandbox tool and the creation of Digital Learning Objects.  Specifically, you can use these How-Tos to learn:</p>

<h3 class="h3main"><a href="part-one/">Introducing the WSU Digital Image Collections</a></h3>
<p>Discover some of the key subject areas covered by our featured collections, <i>Virtual Motor City</i> and <i>Digital Dress</i>.  Discover how to use the strengths of these collections to build dynamic and visually-based learning objects.</p>

<h3 class="h3main"><a href="part-two/">Finding, Retrieving, and Storing Images</a></h3>
<p>With our video tutorials, you'll learn how to keyword search, browse subjects, create portfolios, and use our export tool to begin gathering and building your Digital Learning Objects.  The tutorials use <i>Virtual Motor City</i> as an example, but their basic concepts can be applied to any of WSU's Digital Collections.</p>

<h3 class="h3main"><a href="part-three/">Building and Assessing Digital Learning Objects</a></h3>
<p>Whether a seasoned-pro or just beginning to create Digital Learning Objects, you'll find these videos, guides and checklists helpful in your effort to create effective, outcome-based learning objects.</p>

