<h2 class="h2main">Searching the <i>Virtual Motor City Collection</i></h2>
<p class="style4">The Virtual Motor City Collection consists of approximately 38,000 images which represents a subset of the more than 800,000 images from The Detroit News given to The Walter Reuther Library at Wayne State University in 1997.&nbsp; There are a number of ways to locate images within the collection which are explained below.</p>
 <!-- If you are looking to browse a broad category to see what types of images are in the collection, a good way to start is to enter through the <strong>Browse Topics</strong> portal.&nbsp; Here we have divided the collection into broad topics such as Architecture, Arts &amp; Entertainment, Auto Industry, Business, Cities and Towns, Culture and Social Life, Government, Social Movements, Sports and Recreation, Transportation and War Military. </p>
<p class="style4"><img src="images/part_four/ii/search1.jpg" alt="Browse topics portal" width="550" height="287" /></p>
<p class="style4"><img src="images/part_four/ii/search2.jpg" alt="Browse Arts &amp; Entertainment" width="550" height="451" /></p>-->
<p class="style4">If you know what you are looking for and wish to see if any images are available in the collection, you may wish to try a <strong>Keyword Search</strong>.&nbsp; To do this go to VMC home page and on the left of the screen, &ldquo;Search the collection.&rdquo;&nbsp; Simply type in the term you&rsquo;d like to search, click in the box that reads, &ldquo;only display records that have digital media&rdquo; and click &ldquo;Search&rdquo; (see below).</p>
<p class="style4"><img src="../../inc/images/part_four/ii/Search3.jpg" alt="Keyword Search" width="550" height="300" /></p>
<p class="style4">This search will take you to a results page with 20 images displayed per page.&nbsp; You&rsquo;ll note in this example some hits are for the person Joe Louis and some are for &ldquo;Joe Louis Arena.&rdquo;</p>
<p class="style4"><img src="../../inc/images/part_four/ii/search4.jpg" alt="Results Joe Louis" width="550" height="300" /></p>
<p class="style4">From here you can select the images that you&rsquo;d like to move to a portfolio by clicking in the box in the lower right corner of the image.&nbsp; Once you&rsquo;ve chosen the images you wish to save, click the box in the upper right corner labeled, &ldquo;add checked to portfolio.&rdquo;&nbsp; Name your portfolio and check the box &ldquo;viewable by public,&rdquo; and click &ldquo;Submit&rdquo; (see below).&nbsp; You will then see the portfolio that you created and the images associated with it. </p>
<p class="style4"><img src="../../inc/images/part_four/ii/Search5.jpg" alt="Add to portfolio" width="550" height="350" /><img src="../../inc/images/part_four/ii/Search6.jpg" alt="Images for portfolio" width="550" height="350" /></p>
<p class="style4">Finally, if you would like to search by <strong>Subject</strong> you may (from the home page) click on <strong>LC Subjects</strong> (see below).</p>
<p class="style4"><img src="../../inc/images/part_four/ii/Search7.jpg" alt="Subject search" width="550" height="350" /></p>
<p class="style4"><img src="../../inc/images/part_four/ii/Search8.jpg" alt="Browse LC subject" width="550" height="350" /></p>
<p class="style4">Subjects are arranged in alphabetical order across the top of the screen.&nbsp; The screen is further divided by images on one the left side and subject terms on the right.&nbsp; Click on the term that you are interested in and the records associated with that term will come up, (see below).</p>
<p class="style4"><img src="../../inc/images/part_four/ii/Search9.jpg" alt="Results actors" width="550" height="350" /></p>
