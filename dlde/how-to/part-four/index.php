<p>Learning the strategies of searching the WSU Digital Collections gives users the skills necessary to successfully search and access the many digital resources that will enhance and reinforce projects.  For example, researching the life of Matilda Dodge.</p>

<h3>Why settle for this?</h3>
<p><strong>Matilda Dodge</strong></p>
<div class="paragraphs">
<p>Oakland  University benefactor Matilda Dodge Wilson was born Matilda Rausch in 1883 in  Walkerton, Ontario, Canada, the daughter of German immigrants. After graduating  from the Gorsline Business College in 1902, Matilda went to work as a secretary  for Dodge Brothers Motor Cars.</p>
</div>
<div class="paragraphs">
<p>In 1907, Matilda married her boss, John Francis Dodge. Matilda and John Dodge had  three children: Frances, Daniel and Anna Margaret. The family often went on  weekend retreats to a country property they had acquired in 1908 and named  Meadow Brook Farms. Five years after John Dodge's death in 1920 from  complications associated with influenza, Matilda married lumber broker Alfred  G. Wilson in 1925.</p>
</div>
<div class="paragraphs">
<p>Matilda decided to expand Meadow Brook Farm  and build Meadow Brook Hall - a grand Tudor-revival style mansion based.  Matilda lived in the mansion until her death in 1967. </p>
</div>

